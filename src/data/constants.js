const VALID_CHIP_VALUES = CHIP_GROUPS.reduce((acc, group) => {
  group.subsections.forEach((sub) => {
    acc[sub.id] = new Set(sub.chips.map((chip) => chip.value))
  })
  return acc
}, {})

function sanitizePresetChips(chips) {
  const out = {}
  Object.entries(chips ?? {}).forEach(([groupId, values]) => {
    const validSet = VALID_CHIP_VALUES[groupId]
    if (!validSet || !Array.isArray(values)) return
    const filtered = values.filter((v) => validSet.has(v))
    if (filtered.length > 0) out[groupId] = filtered
  })
  return out
}

